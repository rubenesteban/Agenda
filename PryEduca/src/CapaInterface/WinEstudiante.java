    private void BtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearActionPerformed
        // TODO add your handling code here:
        int k=WinEduca.lst.numeroest;
        boolean bandera=false;
        cedula=TxtCedula.getText();
        nombre=TxtNombre.getText();
        for(int i=0; i<k; i++){
            if(WinEduca.lst.lstest[i].getCedula().equals(cedula)){
                bandera=true;
            }
        }
        if(!bandera){
            WinEduca.lst.lstest[k]= new ClsEstudiante(cedula,nombre);
            LblMensaje.setText("CREACION EXITOSA");
        }else{
            LblMensaje.setText("ESTUDIANTE EXISTENTE");
            BtnListar.setEnabled(false);
        }
        k++;
        WinEduca.lst.numeroest=k;
        TxtCedula.setText(null);
        TxtNombre.setText(null);
        TxtCedula.requestFocus();
        
    }//GEN-LAST:event_BtnCrearActionPerformed

